namespace LeetCodeProblems.MergeTwoSortedLists
{
    /// <summary>
    /// Linked List Node
    /// </summary>
    public class ListNode
    {
        /// <summary>
        /// Value of node
        /// </summary>
        public int val;

        /// <summary>
        /// Next node in the list
        /// </summary>
        public ListNode next;

        /// <summary>
        /// Constructor
        /// </summary>
        /// <param name="x">Value of node</param>
        public ListNode(int x)
        {
            val = x;
        }
    }
}
